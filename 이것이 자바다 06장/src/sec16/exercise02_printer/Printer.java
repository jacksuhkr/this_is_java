package sec16.exercise02_printer;

public class Printer {
	//작성 위치
	void println(int interger) {
		System.out.println(interger);
	}
	void println(boolean truth) {
		System.out.println(truth);
	}
	void println(double real) {
		System.out.println(real);
	}
	void println(String literal) {
		System.out.println(literal);
	}
}
